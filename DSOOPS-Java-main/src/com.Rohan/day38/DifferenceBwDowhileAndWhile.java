public class DifferenceBwDowhileAndWhile {
    public static void main(String[] args) {
        System.out.println("While loop: ");
        int i = 5;
        while(i<4){
            System.out.println("Hello world");
        }

        System.out.println();
        System.out.println("Do-While: ");
        do{
            System.out.println("Hello world");
        }while (i<4);
    }
}
